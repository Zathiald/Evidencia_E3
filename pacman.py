# Función para dibujar un cuadrado en una posición dada
def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# Función para traducir las coordenadas cartesianas del mundo del juego en índices de la lista tiles
def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

# Función para comprobar si un punto dado es válido en el diseño del nivel
def valid(point):
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# Función para dibujar el mundo del juego
def world():
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# Función para controlar el movimiento de pacman y los fantasmas
def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            #Direcciones posibles a las que un fantasma puede moverse si encuentra un obstáculo.
            options = [
                vector(9, 0), # Aumentar la velocidad en la coordenada x
                vector(-9, 0), # Aumentar la velocidad en la coordenada x (en dirección opuesta)
                vector(0, 9), # Aumentar la velocidad en la coordenada y
                vector(0, -9), # Aumentar la velocidad en la coordenada y (en dirección opuesta
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

# Función para cambiar la dirección de pacman si es válida
def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

# Configuración inicial del juego
